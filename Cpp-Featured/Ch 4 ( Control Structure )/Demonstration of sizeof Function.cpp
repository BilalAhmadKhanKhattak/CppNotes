

// CPP Program 
// Demonstrating the use of sizeof() Function.

#include<bits/stdc++.h>

using namespace std;

int main()
{
	cout<<"Data type"<<setw(15)<<"Bytes"<<endl<<endl;
	
	cout<<"int"<<setw(20)<<sizeof(int)<<endl;
	cout<<"float"<<setw(20)<<sizeof(float)<<endl;
	cout<<"double"<<setw(20)<<sizeof(double)<<endl;
	cout<<"char"<<setw(20)<<sizeof(char)<<endl;
	
	return 0;
}

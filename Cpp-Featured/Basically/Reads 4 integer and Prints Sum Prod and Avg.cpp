


// CPP program
// Reads 4 integers
// Prints their sum, prod and average


#include<bits/stdc++.h>

using namespace std;

int main ()

{
	int a, b, c, d, sum, prod, average;
	
	cout<<"Enter four numbers"<<endl;
	cin>>a>>b>>c>>d;
	
	sum=a+b+c+d;
	prod=a+b+c+d;
	average=sum/4;
	
	cout<<"Sum is = "<<sum<<endl;
	cout<<"Prod is = "<<prod<<endl;
	cout<<"Average is = "<<average<<endl;
	
}
